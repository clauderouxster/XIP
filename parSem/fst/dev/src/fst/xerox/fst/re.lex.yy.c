finfo *new_re_lex_buffer(finfo *fi, FILE *f, ifile *file, int line_offset)
{
  finfo *old;
  fi->file.name = strdup(file->name);
  fi->file.type = file->type;
  fi->line_number = /* 0; */ line_offset;
  old = regex_file_info;
  regex_file_info = fi;

  /* We open a new (text) stream for reading.  For that we have to
   * also make a new buffer for lex.  We return here the previous lex
   * buffer, so that it can be reset later by function old_re_input_file.
   * The old previous buffer may still contain something that is not
   * yet handled, especially when we read real files.  Only keyboard
   * (stdin) stream is usually unbuffered.  But not necessarily when
   * it is redirected (see main function in file fsc.c for more
   * details).  Variable print_prompt affects on whether the prompt is
   * printed or not.  When it is less than zero no prompts are
   * printed.  */

  yy_switch_to_buffer(yy_create_buffer(yyin=f,YY_BUF_SIZE));
  fi->buffer = YY_CURRENT_BUFFER;
  if (fi->buffer)
    fi->ch_buf = YY_CURRENT_BUFFER->yy_ch_buf;
  else
    fi->ch_buf = 0;
  return old;
}

