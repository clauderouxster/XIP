void recover_regex_error()
{

  /* We have found an error here and next we try to prevent LEX to
   * read any further before we have stopped YACC.  If the error has
   * occured in the end of regular expression (i.e. yytext == ";"), we
   * return semicolon back to the input stream, because we don't want
   * to miss it and clear the next expression too (see. clean_regex).
   * */

  BEGIN(FORCE_REGEX_ERROR);
  if (*yytext == ';')	cond_unput(';');
  else			cond_unput(' ');
}

int clean_regex()
{

  /* We have now stopped YACC, and we clean the rest of the regular
   * expression.  When reading from keyboard (stdin) we remove only
   * the rest of the line (is it a good solution?).  */

  int i;

  if ((yyin == stdin) && (print_prompt > 0))
    {
      clean_inputline();
      BEGIN(REGEXP0);
      return(END);
    }
  else
    {
      BEGIN(REGEXP);
      while ((i=yylex()) != END)
	if (i == SEMIC) break;
      clean_inputline();
      return i;
    }
}

void stop_regex()
{

  /* We have detected too many errors and we want to stop reading
   * regular expressions.  Here we need only check if we are reading
   * keyboard (stdin) and then clean the rest of the line.  If we are
   * reading a file, we need not do anything here (just to close the
   * file later).  IF we are reading redirected file variable
   * print_prompt is supposed to be zero, and then we need to clean up
   * till next END.  */

  if ((yyin == stdin) && (print_prompt > 0))
    clean_inputline();
  else if (remember_to_clean)
    {
      BEGIN(REGEXP0);
      while (yylex() != END) ;
    }
}
